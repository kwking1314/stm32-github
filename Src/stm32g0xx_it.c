/**
  * @brief This function handles USART2 global interrupt / USART2 wake-up interrupt through EXTI line 26.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) == SET)
  {
    uint16_t temp = 0;
    __HAL_UART_CLEAR_IDLEFLAG(&huart2);
    HAL_UART_DMAStop(&huart2);
    temp = huart2.Instance->ISR;
    temp = huart2.Instance->RDR;
    temp = hdma_usart2_rx.Instance->CNDTR;
    Usart2type.UsartDMARecLen = USART_DMA_REC_SIE - temp;
    HAL_UART_RxCpltCallback(&huart2);
  }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
  HAL_UART_Receive_DMA(&huart2, Usart2type.UsartDMARecBuffer, USART_DMA_REC_SIE);
  /* USER CODE END USART2_IRQn 1 */
}

/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART2)
  {
    if (Usart2type.UsartRecLen > 0)
    {
      memcpy(&Usart2type.UsartRecBuffer[Usart2type.UsartRecLen],
             Usart2type.UsartDMARecBuffer,
             Usart2type.UsartDMARecLen);
      Usart2type.UsartRecLen += Usart2type.UsartDMARecLen;
    }
    else
    {
      memcpy(Usart2type.UsartRecBuffer,
             Usart2type.UsartDMARecBuffer,
             Usart2type.UsartDMARecLen);
      Usart2type.UsartRecLen = Usart2type.UsartDMARecLen;
    }
    memset(Usart2type.UsartDMARecBuffer, 0x00, sizeof(Usart2type.UsartDMARecBuffer));
    Usart2type.UsartRecFlag = 1;
  }
  else if(huart->Instance == USART1)
  {
	// 当接收的最后一个字符为'\n'作为结束符
		if(usart1Recv.recvData == '\n')
			usart1Recv.recvFlag = 1;  // 接收成功标志位
		else // 接受的数据保存到缓冲区
			usart1Recv.recvBuff[usart1Recv.recvNum++] = usart1Recv.recvData;  
		
		// 重新开启串口接收中断
		HAL_UART_Receive_IT(&huart1, (uint8_t *)&usart1Recv.recvData, 1);
  }
}

